package casa;

/**
 *
 * @author luizh
 */
public class Pessoa {
    private String nome;
    private int idade;

    /**
     * @return the nome
     */
    public String getNome() {
        return nome;
    }

    /**
     * @param nome the nome to set
     */
    public void setNome(String nome) {
        this.nome = nome;
    }

    /**
     * @return the idade
     */
    public int getIdade() {
        return idade;
    }

    /**
     * @param idade the idade to set
     */
    public void setIdade(int idade) {
        this.idade = idade;
    }
    
    public void fazAniversario() {
        this.setIdade(this.getIdade() + 1);
    }
    
}
